    w.word as phrasal_verb,
    m.meaning,
    m.extra_meaning,
    s.eng_sentence as eng_example,
    s.kor_sentence as kor_example
FROM word w
LEFT JOIN meaning m ON w.id = m.word_id
LEFT JOIN word_sentence_mapping wsm ON w.id = wsm.word_id
LEFT JOIN sentence s ON wsm.sentence_id = s.id
WHERE w.is_phrasal_verb = 1
ORDER BY w.word;